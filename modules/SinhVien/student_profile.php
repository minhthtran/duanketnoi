                divElement.style.display="block";
            })
            btnCancle.forEach(function (divElement) {
                divElement.style.display="block";
            })
        });

        btnCancle.addEventListener("click", function () {
            addAchivButton.forEach(function (divElement) {
                divElement.style.display = "none";
