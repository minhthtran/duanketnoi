        // open - close change password
        function openModal() {
            const modal = document.querySelector(".modal-content");
            modal.style.display = "block";
        }

        function closeModal() {
            const modal = document.querySelector(".modal-content");
            modal.style.display = "none";
        }

        function addModalEventListeners() {
            const openModalButton = document.getElementById("openModalButton");
            const closeModalButton = document.getElementById("closeModal");
            const cancelButton = document.querySelector(".btn-cancle");

            openModalButton.addEventListener("click", openModal);
            closeModalButton.addEventListener("click", closeModal);
            cancelButton.addEventListener("click", closeModal);
        }
        addModalEventListeners();
        //Hide - display input
        const addGpaButton = document.getElementById("addGpaButton");
        const addAchivButton = document.querySelectorAll(".button-add-achievements");
